    </p>
    
    <ul class="list-disc pl-6 mb-6">
      <li><strong>Weather events:</strong> Hail, windstorms, falling tree branches, or ice damage</li>
      <li><strong>Vandalism:</strong> Intentional damage or break-ins</li>
      <li><strong>Fire:</strong> Windows damaged by fire or smoke</li>
      <li><strong>Accidental damage:</strong> Some policies cover accidental breakage, but this varies</li>
    </ul>

    <h2 class="text-2xl font-semibold mt-8 mb-4">When Coverage May Be Limited or Denied</h2>
    
    <p class="mb-4">
      Insurance typically does NOT cover:
    </p>
    
    <ul class="list-disc pl-6 mb-6">
      <li><strong>Normal wear and tear:</strong> Gradual deterioration or aging</li>
      <li><strong>Lack of maintenance:</strong> Damage from deferred repairs</li>
      <li><strong>Intentional damage:</strong> Damage caused by the homeowner</li>
      <li><strong>Flood damage:</strong> Requires separate flood insurance</li>
      <li><strong>Earthquake damage:</strong> Requires separate earthquake coverage</li>
    </ul>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Indiana-Specific Considerations</h2>
    
    <p class="mb-4">
      Northern Indiana homeowners face unique challenges:
    </p>
    
    <ul class="list-disc pl-6 mb-6">
      <li><strong>Freeze-thaw cycles:</strong> Ice expansion can crack windows, but coverage depends on whether it's considered sudden damage or gradual deterioration</li>
      <li><strong>Severe storms:</strong> Lake-effect weather can cause wind and hail damage</li>
      <li><strong>Deductibles:</strong> Many policies have separate wind/hail deductibles that may be higher than standard deductibles</li>
    </ul>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Filing a Claim: What to Expect</h2>
    
    <p class="mb-4">
