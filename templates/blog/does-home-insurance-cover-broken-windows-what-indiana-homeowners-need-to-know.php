    </p>
    
    <ol class="list-decimal pl-6 mb-6 space-y-2">
      <li>Document the damage with photos and notes</li>
      <li>Contact your insurance company promptly</li>
      <li>An adjuster will inspect the damage</li>
      <li>You'll receive a claim decision and settlement amount</li>
      <li>You may need to pay your deductible before coverage applies</li>
    </ol>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Replacement vs. Repair</h2>
    
    <p class="mb-4">
