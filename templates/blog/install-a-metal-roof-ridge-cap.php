    <h2 class="text-2xl font-semibold mt-8 mb-4">What is a Ridge Cap?</h2>
    <p class="mb-4">
      A ridge cap is the finishing piece that covers the peak (ridge) of your roof where two roof planes meet. 
      For metal roofing, the ridge cap is crucial for weatherproofing and ventilation. In Northern Indiana's climate, 
      where we experience lake-effect snow and extreme temperature swings, a proper ridge cap installation can mean 
      the difference between a roof that lasts 50+ years and one that fails prematurely.
    </p>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Materials You'll Need</h2>
    <ul class="list-disc pl-6 mb-6">
      <li>Metal ridge cap panels (matching your roof material)</li>
      <li>Closure strips or foam seals</li>
      <li>Metal roofing screws with neoprene washers</li>
      <li>Butyl tape or metal roof sealant</li>
      <li>Drill with magnetic bit holder</li>
      <li>Tin snips or metal cutting shears</li>
      <li>Measuring tape and chalk line</li>
      <li>Safety harness and fall protection</li>
    </ul>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Step-by-Step Installation</h2>
    
    <h3 class="text-xl font-semibold mt-6 mb-3">1. Prepare the Ridge</h3>
    <p class="mb-4">
      Clean the ridge area thoroughly, removing any debris, old roofing materials, or ice buildup. 
      In South Bend and surrounding areas, check for ice dam damage before proceeding. 
      Install closure strips along both sides of the ridge to prevent wind-driven rain and snow infiltration.
    </p>

    <h3 class="text-xl font-semibold mt-6 mb-3">2. Measure and Cut</h3>
    <p class="mb-4">
      Measure the ridge length and calculate how many cap pieces you'll need, accounting for 6-inch overlaps. 
      Cut the first and last pieces to fit properly at the gable ends. For Indiana installations, we recommend 
      slightly longer overlaps (8 inches) to handle high winds.
    </p>

    <h3 class="text-xl font-semibold mt-6 mb-3">3. Apply Sealant</h3>
    <p class="mb-4">
      Run a bead of butyl tape or metal roof sealant along the edges where the cap will contact the roof panels. 
      This creates a watertight seal—essential for preventing ice dam water backup during our harsh winters.
    </p>

    <h3 class="text-xl font-semibold mt-6 mb-3">4. Install the Cap</h3>
    <p class="mb-4">
      Starting at one end (typically the gable end opposite prevailing winds), position the first ridge cap piece. 
      Secure with metal roofing screws every 12 inches, driving through both the cap and into the roof decking. 
      Use neoprene-washer screws to prevent leaks.
    </p>

    <h3 class="text-xl font-semibold mt-6 mb-3">5. Overlap and Continue</h3>
    <p class="mb-4">
      Each subsequent piece should overlap the previous one by 6-8 inches. Maintain consistent screw spacing 
      and ensure each piece is aligned properly. For Michiana installations, we pay special attention to 
      wind uplift resistance given our proximity to Lake Michigan.
    </p>

    <h3 class="text-xl font-semibold mt-6 mb-3">6. Seal the Seams</h3>
    <p class="mb-4">
      Apply additional sealant at all overlaps and end caps. This extra step is crucial for Northern Indiana homes 
      to prevent ice infiltration during freeze-thaw cycles.
    </p>

    <h2 class="text-2xl font-semibold mt-8 mb-4">Common Mistakes to Avoid</h2>
    <ul class="list-disc pl-6 mb-6">
      <li><strong>Insufficient overlap:</strong> Can lead to wind-driven rain penetration</li>
      <li><strong>Wrong screws:</strong> Regular screws will rust; use metal roofing screws with washers</li>
      <li><strong>No sealant:</strong> Critical for ice dam prevention in Indiana</li>
      <li><strong>Poor ventilation:</strong> Ridge needs proper airflow to prevent condensation</li>
      <li><strong>Skipping closure strips:</strong> Allows pest and moisture entry</li>
    </ul>

